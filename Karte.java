
public class Karte {

	private int wert;
	private String karte;
	
	public Karte(){
		
	}
	
	public Karte(int wert, String karte) {
		super();
		this.wert = wert;
		this.karte = karte;
	}
	
	public int getWert() {
		return wert;
	}



	public void setWert(int wert) {
		this.wert = wert;
	}



	public String getKarte() {
		return karte;
	}



	public void setKarte(String karte) {
		this.karte = karte;
	}



	
}
